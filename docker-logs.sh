#!/bin/bash
# ============================================
# YuliLog 日志查看脚本
# ============================================

set -e

# 颜色输出
BLUE='\033[0;34m'
NC='\033[0m' # No Color

echo ""
echo -e "${BLUE}============================================${NC}"
echo -e "${BLUE}  YuliLog 工作日志系统 - 实时日志${NC}"
echo -e "${BLUE}============================================${NC}"
echo ""
echo "按 Ctrl+C 退出日志查看"
echo ""

docker compose logs -f
